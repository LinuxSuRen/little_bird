package org.suren.littlebird.exception;

public class SuRenSettingException extends RuntimeException
{

	private static final long	serialVersionUID	= -6314772441331789581L;

	public SuRenSettingException()
	{
		super();
	}

	public SuRenSettingException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public SuRenSettingException(String message)
	{
		super(message);
	}

	public SuRenSettingException(Throwable cause)
	{
		super(cause);
	}

}
